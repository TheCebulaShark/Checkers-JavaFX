package com.example.checkerajavafx;

public class PiecePosition {
    private int x;
    private int y;

    public PiecePosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}